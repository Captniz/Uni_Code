sigma <- 0.431

F <- function(x) {
  ifelse(x < 0, 0, 1 - exp(-x^2 / (2 * sigma^2)))
}

p1 <- F(0.927) - F(-0.927)
cat("p1 = ", p1, "\n")

p2 <- F(0.554) - F(0.331)
cat("p2 = ", p2, "\n")

p3 <- F(0.76) - F(0.615) + F(0.933) - F(0.879)
cat("p3 = ", p3, "\n")

